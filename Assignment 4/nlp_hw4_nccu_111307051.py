def normalize_text(s: str) -> str:
    s = s.lower()
    s = ' '.join(re.findall(r'[a-z0-9]+', s))
    return s

def make_short_label(raw_label: str, question: str) -> str:
    """
    很保守的後處理：
    - yes/no 題：變成單純 "Yes" 或 "No"
    - 其他題：取第一個不是單純 Yes/No 的行，砍到最多 5 個詞
    """
    lines = [ln.strip() for ln in raw_label.splitlines() if ln.strip()]

    if not lines:
        return ""

    # 判斷是不是 yes/no 題
    is_yesno_q = bool(
        re.match(r"^(is|are|can|do|does|did|has|have|was|were)\b",
                 question.strip(), flags=re.IGNORECASE)
    )

    joined = " ".join(lines)
    low = joined.lower()

    if is_yesno_q:
        if "yes" in low and "no" not in low:
            return "Yes"
        if "no" in low and "yes" not in low:
            return "No"
        # fallback：看第一行第一個字
        first_word = lines[0].split()[0].lower()
        if first_word.startswith("y"):
            return "Yes"
        if first_word.startswith("n"):
            return "No"
        # 再不行就用第一行前幾個字
        tokens = lines[0].split()
        return " ".join(tokens[:3])

    # ---- 非 yes/no 題 ----
    # 如果第一行只有 "Yes"/"No"，就改用下一行
    idx = 0
    if lines[0].lower() in ("yes", "no") and len(lines) >= 2:
        idx = 1

    text = lines[idx]
    tokens = text.split()
    return " ".join(tokens[:5])


