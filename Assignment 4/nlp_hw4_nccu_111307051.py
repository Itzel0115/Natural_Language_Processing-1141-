import numpy as np

def hybrid_retrieve(query: str, k_embed: int = 3, k_bm25: int = 3):
    # 1) embedding-based top-k
    embed_docs = retriever.get_relevant_documents(query)
    embed_ids = [d.metadata["id"] for d in embed_docs]

    # 2) BM25 top-k
    q_tokens = query.lower().split()
    scores = bm25.get_scores(q_tokens)
    top_bm25_ids = np.argsort(scores)[::-1][:k_bm25]

    # 3) 合併去重
    final_ids = []
    for i in embed_ids[:k_embed]:
        if i not in final_ids:
            final_ids.append(i)
    for i in top_bm25_ids:
        if int(i) not in final_ids:
            final_ids.append(int(i))

    # 4) 回傳 Document list
    return [docs[j] for j in final_ids]

